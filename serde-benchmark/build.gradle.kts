plugins {
    java
    id("software.amazon.smithy.gradle.smithy-base")
}

smithy {
    format.set(false)
}

val smithyVersion: String by project

dependencies {
    implementation("software.amazon.smithy:smithy-aws-traits:$smithyVersion")
    implementation(project(":smithy-swift-codegen"))
}

fun generateSmithyBuild(): String {
    val projections = makeProjections()
    return """
{
  "version": "1.0",
  "projections": {
${projections}
  }
}
"""
}

data class CodegenTest(val service: String, val module: String)

fun makeProjections(): String {
    val codegenTests = listOf(
        // Serde benchmark models
        CodegenTest(
            "com.amazonaws.sdk.benchmark#AwsJsonRpc10DataPlane",
            "SerdeBenchmarkAWSJSONRPC10"
        ),
        CodegenTest(
            "com.amazonaws.sdk.benchmark#AwsQueryDataPlane",
            "SerdeBenchmarkAWSQuery"
        ),
        CodegenTest(
            "com.amazonaws.sdk.benchmark#AwsRestJsonDataPlane",
            "SerdeBenchmarkAWSRestJSON"
        ),
        CodegenTest(
            "com.amazonaws.sdk.benchmark#AwsRestXmlDataPlane",
            "SerdeBenchmarkAWSRestXML"
        ),
        CodegenTest(
            "com.amazonaws.sdk.benchmark#SmithyRpcV2CborDataPlane",
            "SerdeBenchmarkSmithyRPCV2CBOR"
        ),
    )
    return codegenTests.map { """
      "${it.module}": {
        "transforms": [
          {
            "name": "flattenAndRemoveMixins"
          }
        ],
        "plugins": {
            "swift-codegen": {
              "service": "${it.service}",
              "module": "${it.module.replace("-", "_")}",
              "moduleVersion": "0.0.1",
              "gitRepo": "https://github.com/aws-amplify/smithy-swift.git",
              "author": "Amazon Web Services",
              "homepage": "https://docs.amplify.aws/",
              "sdkId": "${it.service.substringAfterLast("#")}",
              "swiftVersion": "5.9.0",
              "mergeModels": true,
              "copyrightNotice": "//\n// Copyright Amazon.com Inc. or its affiliates.\n// All Rights Reserved.\n//\n// SPDX-License-Identifier: Apache-2.0\n//\n\n// Code generated by smithy-swift-codegen. DO NOT EDIT!\n\n",
              "localDevelopment": true
         }
       }
    }
""" }.joinToString(",\n")
}

tasks.register("generateSmithyBuild") {
    description = "generate smithy-build.json"
    doFirst {
        projectDir.resolve("smithy-build.json").writeText(generateSmithyBuild())
    }
}

tasks.named("smithyBuild") {
    dependsOn("generateSmithyBuild")
}

tasks["jar"].enabled = false

tasks["clean"].doFirst {
    delete("smithy-build.json")
}

